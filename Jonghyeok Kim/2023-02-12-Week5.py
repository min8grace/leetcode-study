        if not root:
            return []
        res = []
        q = collections.deque([root])
        while q:
            level = []
            q_len = len(q)
            for _ in range(q_len):
                node = q.popleft()
                level.append(node.val)
                if node.left:
                    q.append(node.left)
                if node.right:
                    q.append(node.right)
            res.append(level)
        return res
    
