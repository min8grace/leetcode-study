class Solution:
    def invertTree(self, root: Optional[TreeNode]) -> Optional[TreeNode]:
        def invert(node):
            tmp = node.left
            node.left = node.right
            node.right = tmp
        def dfs(node):
            if not node:
                return
            dfs(node.left)
            dfs(node.right)
            invert(node)
        dfs(root)
        return root

