class Solution:
    def maxDepth(self, root: Optional[TreeNode]) -> int:
        maxD = 0
        def dfs(node, d):
            if not node:
                nonlocal maxD
                maxD = max(maxD, d)
                return
            dfs(node.left, d+1)
            dfs(node.right, d+1)
        dfs(root, 0)
        return maxD

