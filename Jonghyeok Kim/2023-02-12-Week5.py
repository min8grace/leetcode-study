class Solution:
    def lowestCommonAncestor(self, root: 'TreeNode', p: 'TreeNode', q: 'TreeNode') -> 'TreeNode':
        stack = [root]
        while stack:
            tmp = stack.pop()
            if tmp.val > p.val and tmp.val > q.val:
                stack.append(tmp.left)
            elif tmp.val < p.val and tmp.val < q.val:
                stack.append(tmp.right)
            else:
                return tmp
