class Solution:
    def isBalanced(self, root: Optional[TreeNode]) -> bool:
        res = 1
        def dfs(node):
            if not node:
                return 0
            left = dfs(node.left)
            right = dfs(node.right)
            nonlocal res
            if abs(left - right) > 1:
                res = 0
            return 1 + max(left, right)
        dfs(root)
        return res

